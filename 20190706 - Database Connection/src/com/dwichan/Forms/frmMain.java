    private void RefreshData() {
        DefaultTableModel mdl = new DefaultTableModel();
        mdl.addColumn("NIM");
        mdl.addColumn("Nama Mahasiswa");
        mdl.addColumn("Jurusan");
        mdl.addColumn("Alamat");
        mdl.addColumn("Telepon");
        
        try {
            Statement st = conn.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM tbl_mahasiswa");
            
            while (rs.next()) {
                Object[] tbl = new Object[5];
                    tbl[0] = rs.getString("nim");
                    tbl[1] = rs.getString("nama");
                    tbl[2] = rs.getString("jurusan");
                    tbl[3] = rs.getString("alamat");
                    tbl[4] = rs.getString("phone");

                mdl.addRow(tbl);
            }
            
            tblData.setModel(mdl);
        } catch (SQLException ex) {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

