    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        switch(jButton4.getText()) {
            case "Add":
                txtNIM.setText("");
                txtNamaMhs.setText("");
                txtJurusan.setSelectedIndex(0);
                txtAlamat.setText("");
                txtTelp.setText("");
                txtNIM.setEditable(true);
                txtNamaMhs.setEditable(true);
                txtAlamat.setEditable(true);
                txtTelp.setEditable(true);
                jButton4.setText("Save");
                break;
            case "Save":
                if (txtNIM.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "NIM wajib diisi!", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (txtNamaMhs.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Nama Mahasiswa wajib diisi!", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (txtAlamat.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Alamat wajib diisi!", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else if (txtTelp.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Nomor Telepon wajib diisi!", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else {
                    txtNIM.setEditable(false);
                    txtNamaMhs.setEditable(false);
                    txtAlamat.setEditable(false);
                    txtTelp.setEditable(false);

                    try {
                        Statement st = conn.getConnection().createStatement();
                        st.executeUpdate("INSERT INTO tbl_mahasiswa VALUES ("
                                + "\"" + txtNIM.getText() + "\","
                                + "\"" + txtNamaMhs.getText() + "\","
                                + "\"" + txtJurusan.getItemAt(txtJurusan.getSelectedIndex()) + "\","
                                + "\"" + txtAlamat.getText() + "\","
                                + "\"" + txtTelp.getText() + "\")");
                        RefreshData();
                        JOptionPane.showMessageDialog(this, "Sulses menambahkan mahasiswa baru!");
                    } catch (SQLException ex) {
                        Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

